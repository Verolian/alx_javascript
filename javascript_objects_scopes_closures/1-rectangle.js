// Rectangle class definition
class Rectangle {
    // Constructor
    constructor(w, h) {
      // Initialize instance attributes
      this.width = w;
      this.height = h;
    }
  }
  module.exports=Rectangle

  // Example usage:
const myRectangle = new Rectangle(5, 8);
console.log(myRectangle.width); // Output: 5
console.log(myRectangle.height); // Output: 8
